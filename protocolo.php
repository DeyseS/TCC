<h3>A cirurgia oral menor é aquela que pode ser realizada dentro do consultório dentário por um dentista especializado. Ela atua em ossos, dentes, bochechas e gengivas que necessitam de alguma intervenção cirúrgica.

Seus riscos são muito baixos, já que a anestesia é local, o procedimento é relativamente simples e o pós-cirúrgico é mais tranquilo. Aliás, ela é tão comum e corriqueira que muitas vezes nem reparamos que se trata de um tratamento cirúrgico.

Quer saber quais são as cirurgias orais que podem ser feitas no próprio consultório odontológico? Então, continue a leitura e confira!</h3>


<h1>Extração de siso</h1>
<h3>A extração do siso ou do dente do juízo, como muitas pessoas chamam, é uma das cirurgias mais realizadas nos consultórios. Isso porque o nascimento desse dente pode entortar os outros, caso não tenha espaço suficiente na boca.

Se o quadro for de siso incluso, ou seja, que ainda não nasceu, mas já está dando problemas, a cirurgia é um pouco mais complexa. No entanto, é feita da mesma maneira. O pós-operatório consiste em repouso, com a orientação de não consumir alimentos e bebidas quentes no primeiro dia.</h3>

<h1>Frenectomia lingual e labial</h1>
<h3>Nada mais é do que a remoção do freio lingual ou labial. No primeiro caso se faz necessária quando essa parte é muito curta ou fibrosa, o que atrapalha na fala do paciente.

Já no segundo, além da estética comprometida, o freio labial pode ocasionar a separação dos incisivos superiores. As duas cirurgias são bem rápidas e não necessitam que a pessoa se afaste da rotina. Elas são indicadas também na infância, o que auxilia na autoestima da criança.

Gengivoplastia
Além do alinhamento dos dentes, a gengiva é fator determinante para uma boa estética do sorriso. Se ela for muito grande ou os dentes muito pequenos, o procedimento de gengivoplastia pode ajudar na harmonização, pois remove o excesso da gengiva fazendo o contorno dos dentes.
O procedimento dura em torno de 30 minutos, é indolor e o pós-operatório precisa de cuidados simples, como manter uma alimentação mais pastosa, evitar exposição ao sol e ficar em repouso 24 horas após o procedimento.

<h1>Biópsia na cavidade oral</h1>
<h3>A biópsia é a retirada de uma amostra de tecido para análise. Com ela é possível confirmar a presença ou não de doenças. Sendo assim, na cirurgia oral menor são as patologias bucais o foco desse procedimento, desde as enfermidades mais brandas até as mais agressivas, como o câncer oral.

Ela pode remover parte da lesão ou todo o tecido lesionado, dependendo do objetivo. Geralmente o paciente não sente dores e pode continuar com sua rotina normal após a intervenção.

